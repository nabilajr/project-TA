    <h2>Asal-usul nama Yogyakarta</h2>

    <img src="jogja tempo dulu.jpg" width="200" height="150" style="center;">
    <p>Peter Carey berpendapat bahwa toponimi Yogyakarta berasal dari kata ‘Ayodhya’ dalam bahasa Sansekerta 
        (bahasa Jawa baru : ‘Ngayodya’), yang mengacu pada ibukota Rama dalam epos Ramayana. Gagasan ini diperkuat 
        oleh buku Thomas Raffles yang sangat terkenal, History of Java, terbit tahun 1871, yang menegaskan bahwa 
        kota ini “diberi nama oleh pendirinya menurut nama Ayudhya, ibukota Rama yang terkenal”, yang kemungkinan 
        hanya didengar Raffles dari tradisi tutur orang Jawa yang ditemuinya. Meskipun tanpa bukti tertulis yang kuat, 
        namun kita tidak bisa mengesampingkan begitu saja pendapat tersebut.</p>

    <p>Noorduyn mengemukakan pendapat tentang asal-usul nama Yogyakarta yang diperkuat dengan bukti-bukti tertulis dari surat, 
        jurnal, memoir, dan catatan-catatan yang dilakukan oleh Belanda. Dalam dokumen-dokumen yang dikumpulkannya, 
        ia menyatakan bahwa nama Ayogya/Yogya sudah ada sebelum kota ini dibangun tahun 1755-1756 oleh Mangkubumi yang 
        kemudian bergelar HB I. Penulisan nama Jogja, Djokjo, Djokja, Jogjo, Djokdjo sudah termuat dalam tulisan-tulisan yang 
        dibuat oleh Belanda sejak tahun 1743, bahkan jauh sebelum peristiwa ‘Palihan Nagari’ Perjanjian Giyanti yang memisahkan 
        Surakarta dan Yogyakarta tahun 1755. Ejaan yang belum dibakukan dalam catatan-catatan Belanda itulah yang memunculkan 
        variasi penulisan Yogya atau Jogja, karena huruf “J” dalam bahasa Belanda dibaca “Y”.</p>

    <p>Dalam beberapa pupuh Babad Giyanti karya Yasadipura I, juga beberapa kali disebutkan nama ‘Ayogya’, ‘Yugya’, ‘Ngajukjeku 
        (Ngajukja iku)’ (pupuh 6 dan 138) sebagai sebuah lokasi di Mataram dimana terdapat pesanggrahan perburuan, 
        tempat di mana Kraton berdiri sekarang. Dalam Babad Tanah Jawi versi macapat, juga disebutkan bahwa Paku Buwana II 
        singgah di pesanggrahan Gerjitawati dan mengubah namanya menjadi Ayogya (pupuh 229).</p>

    <p>Rickfles yang menyanggah argumentasi Yogya berasal dari ‘Ayodhya’ ibukota dalam Ramayana, juga membeberkan tambahan 
        bukti tentang penulisan nama Yogya sebelum pembangunan kota ini. Beliau menyimpulkan bahwa sebelum kota 
        Ngayogyakarta Hadiningrat diumumkan oleh HB I, sudah ada dusun di sana yang bernama Garjitawati dan/atau Yogya atau Ayogya. 
        “Garjita” dalam bahasa Sansekerta artinya “meraung, menyombongkan diri, angkuh, membual”, dalam bahasa Jawa Kuna artinya 
        “gembira,senang, puas”, dalam Bahasa Jawa Baru “sangat puas, gembira, meraskan emosi yang kuat”. Bangunan pesanggrahan 
        di hutan Beringan/alas mBering ini digunakan sebagai tempat istirahat perburuan, yang kemungkinan lokasinya berada tepat 
        di kraton Ngayogyakarta Hadiningrat sekarang ini, dan pesanggrahan ini barangkali sudah ada sejak era Amangkurat II 
        bertahta pada era ibukota Mataram berpindah ke Kartasura.</p>

    <p>Meskipun etimologi kata Yogya masih simpang siur dan tidak bisa dipastikan sejarahnya, setidaknya dalam bahasa Jawa 
        baru dan bahasa Indonesia kata ‘Yogya’ sudah mendapatkan terjemahannya yaitu ‘sesuai, layak, pantas, pas’. 
        Berarti tidaklah berlebihan kalau sementara ini kita sepakat mengartikan Yogya-karta sebagai ‘kota yang layak dan pas 
        untuk dihuni’</p>

