<h2>Sejarah Kota Yogyakarta</h2>
          
                    <img src="keraton jogja.jpg" width="200" height="150" style="float: left;">
                    <p>Kota Yogyakarta adalah kediaman bagi Sultan Hamengkubuwana dan Adipati Paku Alam. 
                    Kota Yogyakarta merupakan salah satu kota terbesar di Indonesia dan kota terbesar 
                    keempat di wilayah Pulau Jawa bagian selatan setelah Bandung, Malang, dan Surakarta 
                    menurut jumlah penduduk.
                    Salah satu kecamatan di Yogyakarta, yaitu Kotagede pernah menjadi pusat Kesultanan Mataram 
                    antara kurun tahun 1575–1640. Keraton (Istana) yang masih berfungsi dalam arti yang 
                    sesungguhnya adalah Keraton Ngayogyakarta dan Puro Paku Alaman, yang merupakan pecahan 
                    dari Kesultanan Mataram.</p>
                    <p>Kota Yogyakarta terletak di lembah tiga sungai, yaitu Sungai Winongo, Sungai Code 
                    (yang membelah kota dan kebudayaan menjadi dua), dan Sungai Gajahwong. Kota ini terletak 
                    pada jarak 600 KM dari Jakarta, 116 KM dari Semarang, dan 65 KM dari Surakarta, 
                    pada jalur persimpangan Bandung – Semarang – Surabaya – Pacitan. Kota ini memiliki 
                    ketinggian sekitar 112 m dpl. Meski terletak di lembah, kota ini jarang mengalami banjir 
                    karena sistem drainase yang tertata rapi yang dibangun oleh pemerintah kolonial, 
                    ditambah dengan giatnya penambahan saluran air yang dikerjakan oleh Pemkot Yogyakarta.</p>
                    <p>Kota Yogyakarta sangat strategis, karena terletak di jalur-jalur utama, yaitu Jalan Lintas 
                    Selatan yang menghubungkan Yogyakarta, Bandung, Surakarta, Surabaya, dan kota-kota di selatan Jawa, 
                    serta jalur Yogyakarta – Semarang, yang menghubungkan Yogyakarta, Magelang, Semarang, dan kota-kota 
                    di lintas tengah Pulau Jawa. Karena itu, angkutan di Yogyakarta cukup memadai untuk memudahkan mobilitas 
                    antara kota-kota tersebut. Kota ini mudah dicapai oleh transportasi darat dan udara, 
                    sedangkan karena lokasinya yang cukup jauh dari laut (27 – 30 KM) menyebabkan tiadanya transportasi air di kota ini.</p>
